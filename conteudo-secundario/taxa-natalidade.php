                    <h2>IBGE registra queda da taxa de natalidade no Brasil</h2>
                    <img class="imagem-principal" src="../imagens/natalidade.png" alt="">

                    <div class="texto">

                    <p>
                    Dados de 2021 do Instituto Brasileiro de Geografia e Estatística registraram um recorde de mortes, o maior desde 
                    1974, e queda de nascimentos, a maior desde 2003. “Ao contrário do que foi feito no último governo, é preciso
                     levar a sério essas informações estatísticas que órgãos públicos brasileiros produzem. Elas são ouro para a 
                     gente implementar políticas públicas de maneira mais eficiente, de maneira mais ampla”, comenta o professor 
                     Fábio Betioli Contel, do Departamento de Geografia Humana da Faculdade de Filosofia, Letras e Ciências Humanas
                      da USP. Para o profissional, pode-se ressaltar dois pontos importantes para esses processos: a pandemia e a 
                      queda da taxa de fecundidade.
                    </p>

                    <p>
                    A pandemia da covid-19 mudou o mundo e, consequentemente, o perfil demográfico. “Na história da geografia da 
                    população, pandemias são tidas como eventos de supermortalidade em populações. Então, historicamente, fomes, 
                    guerras e epidemias ou pandemias causam transtornos bastante importantes nesses padrões demográficos. No mundo
                     inteiro, as estatísticas mostraram que algo em torno de 15 milhões de pessoas perderam a vida em consequência
                      direta da pandemia e, no caso brasileiro, foram registrados quase 700 mil óbitos, é um número extremamente 
                      expressivo.
                    </p>

                    <p>
                    Para alguns meses desse período mais difícil da pandemia, cerca de metade da população que morreu teve relação
                     direta com a covid-19. Esse é um quadro geral, que ajuda a explicar porque aumentou tanto a quantidade de 
                     óbitos”, diz o professor. Felizmente, com a implementação de medidas sanitárias, como a quarentena e o uso
                      obrigatório de máscaras, além da vacinação, os números diminuíram. “Soa um pouco paradoxal, porque ao mesmo 
                      tempo tem o aumento das mortes e a diminuição dos nascimentos. Acho que o principal fator para explicar essa 
                      diminuição da natalidade é a diminuição da taxa de fecundidade na população brasileira“, explica Contel.
                     </p>

                     <h2>Natalidade</h2>

                    <p>
                    A taxa de fecundidade corresponde ao número de filhos vivos nascidos por mulher na idade reprodutiva.
                     “Na verdade, desde a década de 70, 80, vem diminuindo a taxa de fecundidade no território brasileiro de forma
                      significativa. Na década de 60, por exemplo, essa taxa estava em torno de seis filhos por mulher; na década
                       de 80, eram quatro filhos por mulher; no ano de 2000, essa taxa de fecundidade era de 2,2 e, em 2020, uma 
                       média de 1,65 filhos. Nós temos um comportamento demográfico hoje, principalmente nesse quesito de fecundidade,
                        muito semelhante à maior parte dos países ricos. Essa taxa de fecundidade vinha diminuindo já por uma série de
                         fatores estruturais, mas numa temporalidade diferente”, coloca Contel.
                    </p>

                    <p>
                    Fatores como o acesso ao sistema de saúde e à educação são fundamentais na promoção da transição demográfica e 
                    são, em parte, responsabilidade da urbanização. Por isso, é importante valorizar os dados do IBGE: “Ao invés 
                    destes últimos negacionismos, incluindo os negacionismos estatísticos que, infelizmente, foram moda no Brasil 
                    recentemente, a gente leva muito a sério esses dados. Eles são a condição básica, a condição fundamental para a 
                    gente implementar políticas de aposentadoria, de saúde, de educação, de moradia. São a condição fundamental para
                     que a gente planeje tudo isso que é tão importante para o povo brasileiro”, pontua o professor.
                    </p>

