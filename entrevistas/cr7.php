                     declarou CR7, em entrevista conduzida pelo Al Nassr.
                    </p>

                    <p>
                    Eu sou um jogador único. É bom vir para cá. Eu bati alguns recordes lá, quero bater alguns aqui. Esse contrato é
                     único, porque eu sou um jogador único. Para mim é isso".
                    </p>


                    <p>
                    Cristiano revelou na entrevista que foi procurado por clubes de diferentes países, dentre eles do Brasil. Sem 
                    mencionar os nomes, o craque também afirmou que recebeu ofertas de times de Portugal, Austrália e Estados Unidos.
                     Porém, escolheu o Al-Nassr para poder contribuir de outras formas.
                    </p>


                    <p>
                    — Agora é uma grande oportunidade, não só no futebol, para mudar a mentalidade das novas gerações. Posso dizer
                     que tive muitas oportunidades na Europa, em vários clubes, no Brasil, na Austrália, nos Estados Unidos, até em
                      Portugal, muitos clubes tentaram, mas escolhi esse clube para desenvolver não só o futebol, mas outras partes 
                      da sociedade. Sei o que quero e o que não quero — disse CR7.
                    </p>


                    <p>
                    O jogador garantiu que pode entrar em campo nesta quinta, contra o Al Tai, pela Liga Saudita.
                    </p>

                    <p>
                    "Sei que a liga é muito competitiva. As pessoas ainda não sabem disso, mas eu sei. Vi vários jogos. O que eu 
                    quero é jogar. Quero jogar depois de amanhã, mas é com o técnico", declarou.
                    </p>

                    <p>
                    Também presente no evento, o técnico Rudi Garcia brincou com o fato de ter muitos jornalistas para a entrevista,
                     e disse que é “uma honra trabalhar com uma das lendas do futebol”.
                    </p>

                    <p>
                    Cristiano Ronaldo chegou ao Mrsool Park (Estádio Universitário Rei Saud) pouco depois das 13h (de Brasília) e fez
