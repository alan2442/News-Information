                      primeiro contato com o elenco da equipe, que conta com os brasileiros Luiz Gustavo, ex-Bayern de Munique, e 
                      Anderson Talisca, ex-Bahia e Benfica.
                    </p>

                    <p>
                    Em seguida, o craque vestiu a camisa 7 de sua nova equipe e subiu ao campo para ser ovacionado pela torcida. O
                     Al-Nassr vendeu ingressos por cerca de R$ 21. Todo o valor arrecadado será doado para a Ehsan, plataforma do
                      governo do país desenvolvida para caridade.
                    </p>

                    <p>
                    Em cerca de 30 minutos, Cristiano Ronaldo teve o seu primeiro contato com os fãs sauditas. Ele reforçou seu 
