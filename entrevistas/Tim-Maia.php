                    <h2>Entrevista com Tim Maia</h2>
                    <img class="imagem-principal" src="../imagens/tim-maia.jpg" alt="">

                    <div class="texto">
                    <p>
                    Sebastião Rodrigues Maia é o seu nome de batismo, mas ficou conhecido mesmo como Tim Maia, era dono de uma voz 
                    rouca e potente, foi um dos pioneiros da música Soul no Brasil, características de uma personalidade marcante 
                    e de atitudes polêmicas. Assim era Tim Maia, um talento sem medições para a música e para as confusões.
                    </p>

                    <p>
                    De uma longa entrevista, só um trecho muito pequeno havia sido publicado. Durante a conversa, em 1995, o cantor
                     não poupou ninguém. Sobrou para Roberto Marinho, Boni, Jorge Ben Jor, Roberto Carlos, Fausto Silva, Silvio 
                     Santos, João Gilberto, Marisa Monte, Lulu Santos, Sandra de Sá, Ed Motta, Fernando Gabeira, Jair Rodrigues, o
                      Santo Daime e até para a própria banda
                    </p>

                    <h2>Marcio Gaspar</h2>

                    <p>
                    Era o início do primeiro mandato de Fernando Henrique Cardoso como presidente. O Plano Real completava um ano,
                     com governo e população comemorando a queda da inflação − de 40% para os então quase inacreditáveis 2% ao mês;
                      o real em paridade com o dólar e a consequente farra da classe média no exterior. Era 1995 e a música 
                      brasileira chorava a recente perda de Tom Jobim (em dezembro de 1994), enquanto os Mamonas Assassinas dominavam
                       o rádio e a TV. Em um apart-hotel dos Jardins, em São Paulo, Tim Maia recebia os jornalistas Marcio Gaspar e 
                       Lauro Lisboa Garcia para uma entrevista cujo mote era o lançamento do disco Nova Era Glacial. Lauro, repórter
                        do “Caderno 2″ de O Estado de S. Paulo, que publicaria poucos dias depois alguns trechos da conversa; e
                         Marcio, da Qualis, efêmera revista especializada em música, que fecharia suas portas antes da publicação da
                          entrevista.
                    </p>

                    <p>
                    A maior parte da memorável conversa com Tim Maia, que morreria três anos depois (em 15 de março de 1998, aos 56 
                    anos), permanecia inédita até agora. É possível ouvir trechos da entrevista no www.revistabrasileiros.com.br e 
                    no blog www.afroencias.com.br. Os jornalistas encontraram um Tim Maia surpreendentemente bem disposto às 9 horas
                     daquela manhã. E como era de seu feitio, o cantor não mediu as palavras. Além do imenso talento musical, a 
                     autenticidade de Tim Maia era outra de suas melhores qualidades.
                    </p>

                    <h2>Marcio Gaspar – Que história é essa de acordar tão cedo? Nova fase, bem mais saudável?</h2>

                    <p>
                    Tim Maia – Não, sempre acordei cedo. Mas agora, tô acordando cedo mesmo porque… eu acho que isso é negócio de 
                    velhice, sabia? Que nem galo. Galo velho empoleira cedo, né? E acorda mais cedo. Acho que é isso, deve ser a 
                    idade. Eu sempre acordei cedo… às vezes, nem dormia (risos).
                    </p>

                    <h2>M.G. – Você costuma ouvir seus discos antigos?</h2>

                    <p>T.M. – Tô ouvindo agora… É o maior barato, sabia? Como sou um cara que sempre grava músicas falando de algo 
                        que aconteceu, aí dá pra lembrar. Lembrar das “cumadizinhas”, dos momentos legais e dos momentos tristes 
                        também.
                    </p>

                    <h2>M.G. – Entre esses discos, qual você acha o mais legal?</h2>

                    <p>
                    T.M. – O cantado em inglês (Tim Maia, 1978) é o que eu gosto mais.
                    </p>

                    <h2>Lauro Lisboa Garcia – Nesse novo disco, você gravou “Corcovado” e “Meditação” em inglês, e já tinha gravado 
                        as duas em português. Por que gravar em inglês?</h2>

                    <p>
                    T.M. – Foi uma homenagem ao Antonio Carlos Jobim. Essa versão que está saindo aí tem cinco anos. A voz em 
                    português, gravei em cima da voz que havia colocado em inglês. É essa aí, no Tim Maia Bossa Nova. Eu tenho 
                    isso em CD também, mas lancei pela Vitória Régia (selo do próprio Tim). Foi o único disco que não dei pra 
                    Continental. Aquilo ali é minha aposentadoria, entendeu?
                    </p>

                    <h2>L.L.G. – Vendeu bem esse disco?</h2>

                    <p>T.M. – Vendeu. Eu sempre digo que vendeu menos, que nem as gravadoras falam pra não pagar direitos 
                        autorais. Eu também digo que vendeu menos. E não tem jeito de provar, né? Nem eles.
                    </p>

                    <h2>M.G. – Quantos discos você calcula que já vendeu até hoje?</h2>

                    <p>
                    T.M. – Acho que bem menos que Chitãozinho e Xororó, viu? O Roberto (Carlos) também vende mais. Mas, mais ou menos
                     que nem Jorge Ben, Fábio Jr., a gente vende assim, igual, na mesma base. Cem mil discos, cada disco. Ainda bem.
                    </p>

