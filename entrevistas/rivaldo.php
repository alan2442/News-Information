                    <h2>Entrevista com Mc Hariel</h2>
                    <img class="imagem-principal" src="../imagens/rivaldo.jpg" alt="">

                    <div class="texto">
                    <p>
                    Numa época em que as comemorações de gols mais lembram coreografias das dançarinas do "É o Tchan", o 
                    meia-atacante Rivaldo, do Barcelona e da seleção brasileira, surpreendeu o mundo no último domingo.
                    </p>

                    <p>
                    Ao comemorar o seu primeiro gol na vitória sobre o Español, pelo campeonato daquele país, Rivaldo levantou sua
                     camisa e, por baixo dela, exibiu uma outra, a do meia brasileiro Giovanni, que vem sendo desprezado pelo técnico
                      holandês Louis Van Gaal.
                    </p>

                    <p>
                    Mais do que um gesto de amizade, a atitude foi uma reação à "máfia holandesa" que domina o time catalão -além de 
                    Van Gaal, há oito jogadores e três integrantes da comissão técnica holandeses.
                    </p>


                    <p>
                    Em entrevista à Folha, por telefone, Rivaldo, 26, disse que só quis animar o amigo e evitou criticar Van Gaal.
                     Mas admitiu que não é mais o jogador conformista de até poucos anos atrás.
                    </p>

                    <h2>
                    Folha - O seu gesto foi muito comentado no Brasil. O que lhe motivou a fazer aquilo?
                    </h2>

                    <p>
                    Rivaldo - Tive a idéia durante a semana, mas vinha pensando fazia bastante tempo, principalmente depois que ele 
                    não vinha mais sendo relacionado para os jogos. Eu via nos treinos que ele estava triste, queria animá-lo. E 
                    acho que valeu a pena, porque ele já está mais contente. Naquele dia mesmo ele me ligou para agradecer.
                    </p>

                    <h2>
                    Folha - A atitude foi também uma provocação ao técnico Louis Van Gaal, que dá preferência aos holandeses da equipe?
                    </h2>

                    <p>
                    Rivaldo - Tenho a consciência de que Giovanni joga muito, mas fiz aquilo para animar ele, e não para pressionar o
                     treinador. Quem sou eu para pressioná-lo? Sei que tenho moral no Barcelona, mas não para interferir no trabalho
                      dele.
                    </p>

                    <h2>
                    Folha - Você não acha que ele protege os holandeses da equipe?
                    </h2>

                    <p>
                    Rivaldo - É difícil de falar. Não acredito que ele vá colocar alguém que não está jogando bem só porque é de seu país.
                    </p>

                    <h2>
                    Folha - Como o seu gesto repercutiu na Espanha?
                    </h2>

                    <p>
                    Rivaldo - Muito bem. As pessoas na rua me param para dizer que não acreditaram naquilo.
                    </p>

                    <h2>
                    Folha - Mas você só fez aquilo porque sabe que tem muita força para a torcida do Barcelona...
                    </h2>

                    <p>
                    Rivaldo - Não fiz só porque a torcida está comigo, fiz porque não tinha nenhum maldade ali, era só para dar
                    alegria a uma pessoa. Mesmo na época do Palmeiras eu faria aquilo se tivesse um companheiro na situação do 
                    Giovanni.
                    </p>

                    <h2>
                    Folha - Mas naquela época você não parecia ter personalidade para fazer algo semelhante...
                    </h2>

                    <p>
                    Rivaldo - Isso aí eu posso falar que tenho, principalmente depois da Olimpíada (de Atlanta-96, quando foi
                    responsabilizado pela eliminação da seleção brasileira, por ter perdido uma bola que originou um gol da Nigéria). 
                    Aguentei muita coisa. A partir dali, resolvi que sempre que tivesse com a razão iria falar, e não mais ouvir 
                    calado. Aprendi a me defender, o que não sabia antes. Fiquei como culpado pelo fracasso brasileiro, mas futebol é
                     um esporte coletivo, a vitória é de todos, assim como a derrota. Não é tênis. Aqui no Barcelona eu perco cinco, 
                     seis bolas por jogo, mas é porque tentei a jogada.
                    </p>

                    <h2>
                    Folha - Com os 55 gols marcados em três temporadas no Espanhol, você é um dos maiores goleadores brasileiros na
                     Espanha. No Barcelona você já é um ídolo maior do que foram Romário e Ronaldinho?
                    </h2>

                    <p>
                    Rivaldo - Considero-me um ídolo, não posso esconder. Todos me param na rua, não posso ir a um shopping. Mas cada 
                    um tem seu momento. Romário e Ronaldo se foram, mas nunca vão ser esquecidos aqui, fizeram história. Tento fazer
                     o mesmo, para que quando eu for seja lembrado também.
                    </p>

