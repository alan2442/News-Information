        <h2>Pesquisa da PUC-Campinas comprova eficácia de laser para recuperação do paladar de pacientes com sequelas da Covid</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia laser para recuperação do paladar-->
            <h3>
            Estudo conduzido no Programa de Pós-Graduação em Ciências da Saúde fez testes em grupo com 70 voluntários por até
             dois meses. Veja quais foram os resultados e os próximos passos.
            </h3>
            <img class="imagem-principal" src="imagens/laser.jpg" alt="" width="100%" >
            <div class="texto">

                <p>
                Uma pesquisa realizada pela PUC-Campinas comprovou a eficácia no uso de laser de baixa potência para recuperar o 
                paladar de pacientes com sequelas da Covid-19. O estudo foi feito por meio do Programa de Pós-Graduação em Ciências
                 da Saúde a partir de testes em grupo com 70 voluntários.
                </p>

                <p>
                A pesquisadora Letícia Fernandes Sobreira Parreira explicou que as avaliações foram realizadas na Clínica de 
                Odontologia da universidade. O objetivo da pesquisa foi verificar os resultados do tratamento que tradicionalmente 
                já era usado em pacientes com câncer com sintomas parecidos.
                </p>

                <h2>Como foi o estudo e resultado</h2>

                <p>
                Os pacientes foram selecionados após a confirmação de que perderam paladar e olfato, e na sequência foram divididos 
                em dois grupos. O primeiro recebeu fotobiomodulação (aplicação de luz) de forma simulada associada à terapia olfativa,
                 enquanto o segundo recebeu aplicação real de laser.
                </p>

                <p>
                Neste caso, a aplicação ocorreu em 18 pontos da borda lateral da língua e glândulas salivares. O estudo demonstrou
                 melhora mais rápida e eficiente no grupo que recebeu o tratamento verdadeiro.
                </p>

                <p>
                "O paciente não sente dor, não sente calor, não sente queimação, não sente absolutamente nada", ressaltou Letícia
                </p>

                <p>
                A luz pode ser vermelha ou infravermelha. Ela aumenta a imunidade do paciente, tem efeito antioxidante, e
                 consegue controlar inflamações. "Foi pensado um protocolo praticamente para tratar os pacientes pós-Covid-19",
                  explicou o pesquisador e professor Carlos Eduardo Fontana.
                </p>

                <p>
                Segundo a pesquisa, houve melhora no distúrbio do paladar nos dois grupos. Contudo, a reversão completa só foi
                 verificada em 13,8% dos pacientes submetidos ao procedimento simulado, enquanto o tratamento real alcançou
                  índice de 32,3%, portanto, ultrapassou o dobro.
                </p>

                <p>
                A avaliação da disfunção do paladar, semanal, submeteu os voluntários ao teste de sabores com ácido cítrico (azedo), 
                sacarose (doce), cloreto de sódio (salgado) e extrato seco de boldo (amargo).
                </p>

                <p>
                O resultado do estudo deve ser apresentado à Sociedade Brasileira de Pesquisa Odontológica em setembro. Os 
                procedimentos foram feitos uma vez por semana, acompanhados por testes de sabores e questionário qualitativo, 
                até remissão dos sintomas ou por até dois meses, em ambos os grupos.
                </p>

            </div>
            

