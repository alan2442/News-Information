        <h2>Wi-fi para monitorar a saúde</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia Wi-fi para monitorar a saúde-->
            <h3>
            Baixo custo das redes que já estão disponíveis poderá facilitar implantação do sistema no futuro
            </h3>
            <img class="imagem-principal" src="imagens/wifi.jpg" alt="" width="100%">
            <div class="texto">

                <p>
                Usar as redes wi-fi – que estão disponíveis em quase todos os ambientes médicos e residenciais – para monitorar,
                 em tempo real, sinais vitais como batimentos cardíacos, frequência respiratória ou quedas de pacientes, ainda soa 
                 como ficção científica para boa parte das pessoas. No entanto, a utilização da inteligência artificial com este 
                 objetivo vem avançando no mundo todo, inclusive no Brasil.
                </p>

                <p>
                Como a tecnologia está amplamente difundida, sua aplicação na área da saúde é considerada de baixo custo. Além disso
                , não há necessidade de nenhum tipo de intervenção que afete o indivíduo, como o uso de algum tipo de dispositivo. 
                Entretanto, a rede wi-fi não trabalha sozinha: ela coleta os dados, mas quem processa a informação é a tecnologia CSI
                 (Channel State Information).
                </p>

                <p>
                A tecnologia mapeia as características da pessoa que está sendo monitorada, criando um padrão individualizado cujos 
                dados ficam armazenados em nuvem. Dessa forma, torna-se mais fácil acompanhar o paciente e identificar qualquer 
                alteração, o que pode ser feito pelo profissional de saúde por meio de um celular ou notebook.
                </p>

                <p>
                No Brasil, um grupo de pesquisadores tem se dedicado a esse campo de estudos e cerca de 130 voluntários já cederam
                 seus dados em atividades como caminhar, sentar-se, deitar, entre outras 17 posições. São necessários outros 170 
                 participantes para o prosseguimento da pesquisa, realizada pela Universidade Federal Fluminense (UFF), no Rio de 
                 Janeiro.
                </p>

                <p>
                “Com o uso da inteligência artificial, poderemos ajudar, por exemplo, quem sofre de apneia do sono. A pessoa poderá 
                ser diagnosticada sem aqueles fios desagradáveis acoplados ao corpo. A rede wi-fi será capaz de captar as 
                informações, processá-las e enviá-las sem a necessidade de outros equipamentos”, explica Débora Muchaluat Saade,
                 professora do Departamento de Ciência da Computação da UFF e uma das coordenadoras do projeto.
                </p>

                <p>
                Além do monitoramento de apneia e respiração, o grupo da UFF está especialmente interessado no controle de batimentos
                 cardíacos, fazendo comparações de eficiência entre a tecnologia wi-fi e os relógios do tipo smartwatch. Os estudos
                  são parte da rede de pesquisa em saúde e-Health Rio, projeto apoiado, desde 2019, pela Fundação Carlos Chagas Filho
                   de Amparo à Pesquisa do Rio de Janeiro (Faperj) e pelo Conselho Nacional de Desenvolvimento Científico e
                    Tecnológico (CNPq).
                </p>

            </div>
            

