        <h2>De câncer a gripe, as doenças na mira de novas vacinas de mRNA após covid</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia possibilidade de vacina do cancer e gripe-->
            <h3>
            Apesar de ser estudada há décadas, essa tecnologia fez sua 'estreia' oficial durante a pandemia de covid-19. As altas 
            taxas de eficácia impulsionam agora novas pesquisas em várias áreas da medicina.
            </h3>
            <img class="imagem-principal" src="imagens/vacina.jpg" alt="" width="100%" >
            <div class="texto">

                <p>
                Existem certas ideias que, apesar de promissoras, ficam empacadas e demoram para virar realidade. De certa maneira,
                 as vacinas de mRNA se encaixam nesse conceito: são estudadas há décadas, mas só fizeram uma "estreia oficial" 
                 durante a pandemia de covid-19, quando as farmacêuticas Pfizer/BioNTech e Moderna lançaram os primeiros imunizantes
                  baseados nessa tecnologia.
                </p>

                <p>
                Como você vai entender ao longo deste artigo, essa plataforma que ajudou a conter as ondas de casos, hospitalizações 
                e mortes relacionadas ao coronavírus foi inicialmente pensada como um tratamento contra o câncer há várias décadas.
                </p>

                <p>
                E agora, graças à alta efetividade das vacinas recentes, o mRNA vive uma "era de ouro" e é testado como possível 
                opção terapêutica contra as mais variadas doenças — dos tumores à gripe; do colesterol alto ao lúpus.
                </p>

                <p>
                Mas como essa tecnologia chegou até aqui? E quais são os próximos passos para que ela seja usada em diversas áreas
                 da medicina? Segundo especialistas ouvidos pela BBC News Brasil, as possibilidades nesse campo da ciência são as
                  mais variadas — e dependem apenas do investimento financeiro e da criatividade humana.
                </p>

                <p>
                Mas, para chegarmos a esses detalhes, é preciso antes entender um dos processos mais fundamentais da nossa própria 
                biologia.
                </p>

                <h2>Mini-impressoras dentro de nós</h2>

                <p>
                Com exceção de óvulos e espermatozoides, todas as células do nosso corpo carregam dentro do núcleo o genoma completo, o DNA.
                </p>

                <p>
                Nesse conjunto de cromossomos, estão "escritas" muitas das informações que definem os processos orgânicos, as 
                características físicas e a propensão a determinadas doenças de cada um de nós.
                </p>

                <p>
                Mas o DNA sozinho não faz nada: quando ele precisa enviar algum comando à célula, essa fita em dupla hélice gera 
                uma cópia simples de determinado trecho do código genético.
                </p>

                <p>
                Esse "xerox" genético vem numa fita simples e é o que conhecemos como RNA mensageiro, ou mRNA.
                </p>

                <p>
                Esse material então sai do núcleo e viaja até os ribossomos, no citoplasma da célula. Essa estrutura lê a "receita" 
                genética do mRNA e fabrica uma proteína específica relacionada àquele comando escrito no DNA.
                </p>

                <p>
                Desde que esse mecanismo foi conhecido, a partir dos anos 1960, os cientistas começaram a se perguntar: será que é 
                possível aproveitar essas "mini-impressoras" que carregamos dentro das células para produzir proteínas específicas?
                </p>

                <p>
                O objetivo era que essas proteínas tivessem algum fim terapêutico, e pudessem servir para gerar uma resposta 
                do sistema imunológico — o que permitiria combater o crescimento de um tumor ou a invasão de um vírus mortal, 
                por exemplo.
                </p>


