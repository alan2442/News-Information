        <h2>Lula sanciona lei para assegurar atendimento odontológico pelo SUS</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia lula/sus-->
            <h3>
            Lei relança o programa Brasil Sorridente, criado em 2004, voltado à saúde bucal da população. Governo credenciou quase
             3,7 mil novas equipes que atendarão pacientes pelo país.
            </h3>
            <img class="imagem-principal" src="imagens/sus.jpeg" alt="" width="100%">
            <div class="texto">

                <p>
                O presidente Luiz Inácio Lula da Silva (PT) sancionou nesta segunda-feira (8) a lei que inclui o programa Brasil 
                Sorridente na Lei Orgânica da Saúde. A medida prevê que a população terá direito a atendimento odontológico pelo
                 Sistema Único de Saúde (SUS).
                </p>

                <p>
                Criado em 2004, no primeiro mandato de Lula como presidente, a Política Nacional de Saúde Bucal, chamada de Brasil 
                Sorridente, é um programa que visa a garantir o acesso da população à saúde bucal.
                </p>

                <p>
                É mais um programa que surgiu nas gestões anteriores do PT e que Lula relança em seu terceiro mandato. Foi assim com 
                Bolsa Família, Mais Médicos e Minha Casa, Minha Vida.
                </p>

                <p>
                O governo informou que, com a sanção da lei, a oferta de serviços odontológicos não poderá ser interrompida ou 
                colocada em segundo plano por gestores federais, estaduais e municipais.
                </p>

                <p>
                O Brasil sorridente tem como foco ampliar a oferta desse serviço em regiões vulneráveis e com vazios assistenciais.
                </p>

                <p>
                Com a sanção e o relançamento do programa, o Ministério da Saúde anunciou que credenciou quase 3,7 mil novas equipes
                 de saúde bucal e 630 novos serviços e unidades de atendimento. O governo espera ter 59,7 mil equipes credenciadas 
                 para atender a população até o fim de 2026.
                </p>

                <p>
                A pasta diz que 805 municípios foram contemplados com os novos serviços e equipes de saúde bucal, sendo que 85
                 receberão essas equipes pela primeira vez.
                </p>

                <p>
                De acordo com o ministério, o país passa a ter 33,3 mil equipes trabalhando e 5,6 mil serviços odontológicos em 
                funcionamento. Com as novas habilitações, mais de 10 milhões de brasileiros passam a ser alcançados pelo Brasil
                 Sorridente, chegando a 111,6 milhões de pessoas cobertas pelo programa.
                </p>

                <p>
                O ministério também habilitou 19 novos Centros de Especialidades Odontológicas, 10 novas unidades odontológicas 
                móveis e 552 novos Laboratórios Regionais de Próteses Dentárias (LRPD).
                </p>

