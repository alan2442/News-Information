        <h2>Mercado bilionário: demanda cresce e moda plus size planeja R$ 15 bilhões de faturamento até 2027</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia moda plus size-->
            <h3>
            Avanço é impulsionado por pequenos negócios, que representam mais de 90% do setor.
            </h3>
            <img class="imagem-principal" src="imagens/modaplus.jpg" alt="" width="100%" >
            <div class="texto">

                <p>
                Os debates sobre inclusão e aceitação aos corpos fora do padrão têm crescido no mercado de vestuário. E, impulsionado 
                pelo alcance da internet e das redes sociais, o tema já começa a alterar as configurações do segmento de moda — 
                principalmente no que diz respeito a roupas plus size.
                </p>

                <p>
                Segundo os dados mais recentes da Associação Brasil Plus Size (ABPS), o segmento movimentou R$ 9,6 bilhões em 2021 e
                 tem expectativa de alcançar os R$ 15 bilhões em faturamento até 2027.
                </p>

                <p>
                De acordo com especialistas ouvidos pelo g1, o movimento não é de hoje, mas ganhou força durante a pandemia e tende
                 a crescer ainda mais nos próximos anos — resultado de mudanças na percepção e comportamento dos consumidores que, 
                 mais empoderados, demandam uma maior diversidade de produtos e peças que atendam a diferentes estilos.
                </p>

                <p>
                Lá fora, nomes como Dolce&Gabbana e Versace, por exemplo, já trabalham há anos com tamanhos maiores do que a grade 
                convencional. Por aqui, no entanto, esse movimento é mais recente, e só agora o mercado plus size começa a dar passos
                 mais largos rumo a uma participação maior no setor de vestuário.
                </p>

                <p>
                O crescimento, no entanto, é gradativo. O segmento ainda sente os impactos da pandemia de Covid-19, que trouxe uma 
                queda geral na produção do setor de vestuário. Dados do IEMI – Inteligência de Mercado apontam que a produção de 
                roupas plus size registrou uma queda de 5,4% em 2022 em relação ao ano anterior, com aproximadamente de 178,2 milhões
                 de peças fabricadas.
                </p>

                <p>
                “Houve, por exemplo, todo o impacto da valorização das commodities, que se agravou em 2022 com a guerra na Ucrânia. 
                Além disso, também houve um ajuste de preços, que veio atrasado após um longo período de redução do consumo por conta
                 da inflação”, explica o diretor do IEMI, Marcelo Villin Prado.
                </p>

            </div>
            

