        <h2>Dólar fecha em queda, mesmo com inflação de abril acima das expectativas</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia do Dollar-->
            <h3>
            Nesta sexta-feira, a moeda norte-americana recuou 0,28%, cotada a R$ 4,9356. A semana termina com queda de 0,40%.
            </h3>
            <img class="imagem-principal" src="imagens/dollar.jpg" alt="" width="100%">
            <div class="texto">

                <p>
                O dólar fechou em queda nesta sexta-feira (12), mesmo após a divulgação dos dados de inflação de abril pelo Instituto
                 Brasileiro de Geografia e Estatística (IBGE), que vieram acima do esperado.
                </p>

                <p>
                O Índice de Preços ao Consumidor Amplo (IPCA) teve alta de 0,61% no mês passado, o que representa uma desaceleração 
                da inflação em relação a março, quando o IPCA subiu 0,71%. No entanto, o resultado foi maior do que as projeções do
                 mercado, que esperavam uma alta menos acentuada, de 0,55%.
                </p>

                <p>
                A moeda norte-americana caiu 0,25%, cotada a R$ 4,9234. Na mínima do dia, foi a R$ 4,9084. Veja mais cotações.
                </p>

                <h2>O que está mexendo com os mercados?</h2>

                <p>
                A inflação oficial do país no acumulado em 12 meses, segundo o IBGE, é de 4,18% até abril. Naquele mês, todos os 
                grupos pesquisados tiveram alta no mês, mas o destaque vai para o setor de Saúde e cuidados pessoais, que subiu
                 1,49% em abril e teve impacto de 0,19 ponto percentual no índice cheio.
                </p>

                <p>
                Os combustíveis, no entanto, que foram "vilões" das últimas medições do IBGE, caíram 0,44%, contra alta de 7,01% em 
                março. Desta vez, apenas o etanol teve alta (0,92%), enquanto diesel (-2,25%), gás veicular (-0,83%) e gasolina 
                (-0,52%) caíram.
                </p>

                <p>
                No exterior, as atenções estão voltadas principalmente às discussões nos Estados Unidos sobre a possibilidade de 
                elevar o teto da dívida no país. Em relatório, a XP Investimentos explica que o valor máximo que o país pode emitir 
                de dívida atualmente não pode ultrapassar os US$ 31,4 trilhões.
                </p>

                <p>
                Esse é o limite de emissão de títulos utilizados pelo governo para honrar todas as suas obrigações financeiras, 
                inclusive as despesas ordinárias, como salários de servidores públicos.
                </p>

                <p>
                O presidente Joe Biden quer elevar o teto da dívida para que o país possa arcar com suas despesas, mas há um impasse
                 entre os partidos Democrata e Republicano para negociar a questão, o que vem preocupando os investidores nos últimos
                  dias.
                </p>

                <p>
                Porém, "notícias de que os EUA estão progredindo em um acordo" trouxeram certo alívio aos mercados, que operam com um
                 tom mais positivo neste pregão, destacam analistas do BTG Pactual.
                </p>

            </div>
            

