                <h2>Globo</h2>
                <img class="imagem-principal" src="imagens/globo.png" alt="" width="100%" >
                <p>
                Em 1925, Irineu Marinho fundou um simples jornal impresso no Rio de Janeiro chamado “O Globo”. Provavelmente, ele 
                não esperava que aquele jornal seria a origem de um dos maiores e mais influentes canais de comunicação do mundo.
                 Quando seu filho, Roberto Marinho, assumiu o comando, a história da Rede Globo mudaria completamente.
                </p>

                <p>
                A emissora possui histórico de forte envolvimento político-econômico, influenciou eleições e já foi acusada de 
                participação em esquemas de fraude e corrupção.
                </p>

                <p>
                A Rede Globo surgiu em 1925, a partir do jornal impresso “O Globo”. Irineu Marinho foi o fundador do jornal impresso.
                 Seu filho, Roberto Marinho, tornou-se presidente da empresa em 1931, e conseguiu a concessão para TV aberta com
                  Juscelino Kubitschek em 1957.
                </p>

                <p>
                No mesmo ano da fundação do jornal, Irineu Marinho faleceu devido a um ataque cardíaco.
                </p>

                <p>
                Roberto não se sentiu pronto para comandar o jornal e deixou a tarefa com Euricles de Matos, jornalista com mais
                 experiência. Roberto trabalhou por anos como jornalista n’O Globo.
                </p>

                <p>
                Em 1931, com o falecimento de Euricles, Roberto se tornou presidente do jornal e começou a expansão da empresa. 
                Fator essencial na história da rede.
                </p>

                <p>
                Em 1934, Roberto Marinho fundou a rádio O Globo.
                </p>

                <p>
                A rádio fez muito sucesso na década de 50 por trazer grandes nomes da música de seu tempo.
                </p>

                <p>
                Nessa década, Getúlio Vargas foi eleito presidente do Brasil. Com medo de que o ex-ditador voltasse a tomar
                 medidas totalitárias, uma parcela da população se organizou para fazer  ferrenha oposição ao presidente. 
                 Dentre os grupos do movimento, estava a rádio Globo.
                </p>
                
                <h2>Band</h2>
                <img class="imagem-principal" src="imagens/band.jpg" alt="" width="100%" >

                <p>
                O Brasil de 1967, quando a TV Bandeirantes foi inaugurada, ganhava uma nova constituição criada em pleno regime
                 militar. No discurso inaugural, João Jorge Saad, presidente do grupo, deu um recado: “essa televisão nasceu para 
                 cumprir sua missão em divertir e informar e orientar o povo”. 
                </p>

                <p>
                Dois anos depois, um incêndio destruiu a emissora. As imagens do incêndio foram captadas e transmitidas pelos 
                róprios funcionários. “Era muito triste porque era uma emissora recém-inaugurada e sendo considerado uma referência 
                em toda América Latina com uma mais moderna”, conta Elmo Francfort, especialista em televisão. “Foi justamente 
                próximo da chegada do homem à Lua e isso acabou mobilizando mais ainda; as pessoas já estavam assim em voltas naquela
                 coisa da magia do homem chegando à lua e de repente um incêndio, a comoção foi total”
                </p>

                <p>
                A partir dali, a TV Bandeirantes teve que se reerguer das cinzas. Teve que recomeçar.
                </p>

                <p>
                O Brasil sonhou com a Band ainda em preto e branco, mas sonhar colorido melhorou e muito essa experiência. Pelé, 
                Jairzinho, Tostão e companhia. Era a Copa do tri, a Copa de 70, a primeira transmitida ao vivo e a cores. 
                </p>

                <p>
                Não foi de uma vez, no entanto, que as cores entraram no cotidiano da TV. Aconteceu aos poucos. Em 1973, a
                Band foi a primeira emissora do País com toda programação colorida, mas a novidade tecnológica demorou a
                 entrar na casa da maioria dos brasileiros. Naquela época, ter um aparelho de TV a cores custava muito caro, 
                 era um privilégio.
                </p>

                <p>
                Então funcionava assim: a vizinhança ia assistir na casa de quem tinha. Cada um levava uma coisinha para beber e
                 comer e a festa estava feita. Esse negócio ganhou até nome: televizinho.
                </p>

                <h2>Sbt</h2>
                <img class="imagem-principal" src="imagens/sbt.jpeg" alt="" width="100%" >

                <p>
                Há 40 anos, o SBT foi ao ar pela primeira vez como emissora de TV aberta no Brasil. Foi na noite de uma quarta-feira,
                 19 de agosto de 1981, que o canal fez sua estreia e concretizou um antigo sonho de seu fundador, Silvio Santos. 
                 Durante a cerimônia de lançamento, que foi transmitida ao vivo, o apresentador vibrou: "Quando eu cheguei aqui o 
                 pessoal me cumprimentou e disse: 'Terminou a novela!'. E, realmente, foi uma novela. Uma novela com happy 
                 ending [final feliz]".
                </p>

                <p>
                Para entender como surgiu o SBT (que significa Sistema Brasileiro de Televisão), voltamos algumas décadas para explicar
                 o contexto que permitiu ao empresário, que já possuía a TVS e era dono de parte da TV Record, ter novas concessões 
                 aprovadas pelo governo federal no início dos anos 1980, ainda durante a ditadura militar brasileira (1964-1985),
                  ampliando seus canais para uma rede nacional.
                </p>

                <p>
                À época, Silvio Santos já era um conhecido apresentador de televisão e tinha seu programa exibido na maioria dos 
                Estados brasileiros. Sua estreia ocorreu em 1961, no Vamos Brincar de Forca, da TV Paulista (emissora que seria 
                adquirida pela Globo cinco anos depois). No ano seguinte, alugou um horário aos domingos, até então pouco explorado
                 pelos canais brasileiros, e estreou o Programa Silvio Santos, que foi ganhando mais espaço na grade.
                </p>

                <p>
                Em 1971, quando estava prestes a não ter seu contrato renovado com a Globo, reviveu seu antigo sonho de ter uma 
                emissora própria quando teve oportunidade de comprar 50% das ações da TV Record. À época, sem novas concessões 
                abertas pelo governo, era o mais próximo que conseguiria para atingir seu objetivo. O negócio, porém, não deu 
                certo. Sem saber do fiasco, Roberto Marinho contatou o apresentador e pediu para que seu contrato fosse renovado,
                 desde que não adquirisse ações da concorrente. Silvio Santos aceitou.
                </p>

                <p>
                Seis meses depois, porém, os mesmos 50% das ações da Record foram colocados novamente à venda. O dono do baú não 
                poderia comprá-los por conta de seu vínculo à Globo, e contou com a ajuda de Joaquim Cintra Gordinho, colocando o 
                nome do amigo nos documentos oficiais. Silvio manteve sua parte da Record até 1989, quando a emissora foi comprada 
                por Edir Macedo.
                </p>

                <p>
                A primeira emissora do apresentador foi a TVS (sigla para TV Studios Silvio Santos Ltda.), concedida pelo governo 
                federal em 22 de outubro de 1975, com oficialização do contrato em 22 de dezembro daquele ano. O canal foi ao ar 
                pela primeira vez em 14 de maio de 1976, pela torre da antiga TV Continental. A aquisição de mais emissoras e o 
                lançamento efetivo do SBT ocorreria apenas alguns anos depois.
                </p>

                <h2>RecordTV</h2>
                <img class="imagem-principal" src="imagens/recordtv.jpeg" alt="" width="100%" >

                <p>
                Por volta das 20h do dia 27 de setembro de 1953, um domingo, Sandra Amaral e Hélio Ansaldo foram os apresentadores 
                responsáveis por inaugurar a TV Record, o canal 7, introduzindo um show musical.
                </p>

                <p>
                A data não era totalmente aleatória: a ideia inicial de Paulo Machado de Carvalho, fundador da emissora, era que a 
                estreia ocorresse em 7 de setembro, de forma a agregar o Dia da Independência do Brasil e, ao mesmo tempo, fazer 
                alusão ao número do canal.
                </p>

                <p>
                Parte dos equipamentos necessários para as transmissões, porém, eram importados e não chegaram a tempo, o que gerou 
                o atraso de quase três semanas. À época, o alcance televisivo não era tão grande como o de hoje - Ao todo, existiam 
                menos de 8 mil aparelhos de TV em São Paulo, e estima-se que poucas centenas de pessoas assistiram à emissora 
                naquele dia. 
                </p>

                <p>
                Os primórdios da Record remetem a tempos diferentes. Era comum, por exemplo, a existência de atrações patrocinadas, 
                como os Grandes Espetáculos União, o Grande Teatro Philco 3D e a Grande Ginkana Kibon. Em vez de anúncios coloridos 
                e barulhentos, existiam as garotas-propaganda, que falavam sobre a qualidade dos mais diversos produtos ao vivo - 
                o que por vezes gerava situações inusitadas.
                </p>

                <p>
                Entre algumas das mais lembradas que passaram pelo canal estão Neide Alexandre, Luci Reis, Wilma Chandler e Meire
                Nogueira, além da conhecida Idalina de Oliveira, um dos principais nomes da área à época. Idalina também fez carreira
                 como atriz, estrelando o infantil Capitão 7 ao lado de Ayres Campos a partir de 1954. Outro nome que fazia a alegria
                  dos mais jovens era Waldemar Seyssel, o palhaço Arrelia, que teve um programa no canal.
                </p>

                <p>
                Na área esportiva, realizou sua primeira transmissão interestadual com o Grande Prêmio Brasil, mostrando a corrida de
                 cavalos que ocorria no Rio de Janeiro para o público paulistano no ano de 1956. Por conta do baixo custo em relação
                  a outras produções, do interesse dos anunciantes pelo formato e pelo sucesso de audiência, os programas musicais 
                  acabavam se destacando na grade da emissora. Diversos nomes internacionais chegaram a se apresentar nas telas da 
                  Record em apresentações exibidas do Teatro Record, como Nat King Cole, Ella Fitzgerald e Louis Armstrong.
                </p>

                <h2>RedeTV</h2>
                <img class="imagem-principal" src="imagens/redetv.jpeg" alt="" width="100%" >

                <p>
                A RedeTV! começou suas transmissões em 15 de novembro de 1999, seis meses após a compra das concessões das cinco 
                emissoras próprias da extinta Manchete pelos empresários Amilcare Dallevo Jr. e Marcelo de Carvalho. A programação
                 é voltada principalmente ao entretenimento, com destaque também para o jornalismo e programas esportivos e de 
                 entrevistas.
                </p>

                <p>
                Após a compra, a rede passou a transmitir o que restava da programação da agora extinta Manchete. Aos poucos, as 
                atrações remanescentes foram saindo do ar e novos títulos surgiram. Entre eles, alguns estão até hoje no ar, como 
                o TV Fama, inicialmente com Mariana Kupfer, e o Superop, apresentado por Adriane Galisteu antes de Luciana Gimenez
                 assumir.
                </p>

                <p>
                Também tornaram-se populares os programas de variedades comandados por João Kléber, principalmente pelo quadro Teste
                de Fidelidade. O Programa Amaury Jr. se torna um dos mais tradicionais do colunismo social na TV. Entre 2011 e 2012,
                 em seus últimos anos de vida, Hebe Camargo levou seu tradicional programa do SBT para a RedeTV!.
                </p>

                <p>
                Na dramaturgia, a emissora produziu seriados sem grande repercussão. Os maiores sucessos de audiência foram as séries
                 e as novelas estrangeiras, especialmente Betty, A Feia (2002), que chegou ao segundo lugar de audiência. Entre 2003 
                 e 2012, o humorístico Pânico na TV! também conquistou alta audiência para a emissora aos domingos.
                </p>

                <p>
                Mais recentemente, a RedeTV! passou a enfrentar problemas para manter a audiência. A crise também se estendeu aos 
                bastidores, com atrasos de salários e demissões em massa. Entre os principais contratados, atualmente, está o 
                polêmico apresentador Sikêra Jr, à frente do Alerta Nacional.
                </p>

                <h2>Tv Nazaré</h2>
                <img class="imagem-principal" src="imagens/tvnazare.jpg" alt="" width="100%" height="420px" >

                <p>
                O sonho de possuir uma emissora de televisão católica era antigo na Arquidiocese de Belém. No entanto, até 1998 não 
                se falava concretamente em TV Nazaré, apenas em Rede Vida. Para retransmitir esta emissora católica de São José do
                 Rio Preto (São Paulo) foi construída uma torre de concreto, com mais de 100 metros de altura. Ali foi instalada,
                  no Natal de 1999, com ajuda dos paraenses, por meio de coletas, promoções e doações individuais e de empresas, a
                   antena do Canal 19 – retransmissora da Rede Vida em Belém.
                </p>

                <p>
                Os Bispos da Arquidiocese, porém, queriam mais. Pretendiam uma emissora católica que pudesse gerar programas locais 
                de qualidade para evangelizar e educar o povo do Pará. No dia 06 de outubro de 2000, durante a Celebração dos 50 anos
                 de vida sacerdotal de Dom Vicente Joaquim Zico, o ex-ministro das Comunicações Pimenta da Veiga e o ex-vice 
                 Presidente da República Marco Maciel premiaram a Arquidiocese com a outorga do Canal 30E UHF para Belém.
                </p>

                <p>
                Em 29 de janeiro de 2002, o ex-ministro das Comunicações Pimenta da Veiga assinou o Contrato de Concessão por 15 
                anos, da TV Nazaré como geradora. Na condição de TV Educativa, a emissora não pode obter lucros com as vendas de 
                comerciais. Pode, entretanto, receber apoios culturais.
                </p>

                <p>
                A TV Nazaré foi inaugurada no dia 11 de maio de 2002 com uma solene sessão no Centro de Cultura e Formação Cristã 
                da Arquidiocese de Belém. A cerimônia contou com a presença de várias autoridades eclesiásticas e civis, como a do 
                ex-governador do Estado do Pará Almir Gabriel e a do ex-ministro das Comunicações Juarez Quadros.
                </p>

                <p>
                Aos poucos, a TV Nazaré cresceu em toda a Amazônia Legal. As primeiras concessões foram feitas pelo ex-ministro
                 Juarez Quadros e a maioria outorgada durante o ano de 2003 pelo ex-ministro Miro Teixeira.
                </p>

                <p>
                No dia 28 de maio de 2004 foi inaugurado oficialmente o sinal da TV Nazaré via satélite, através da empresa New Skies
                 Satelites, com a presença do Núncio Apostólico, Dom Lorenzo Baldisseri; Dom Vicente Zico, Arcebispo de Belém; Dom
                  Carlos Verzeletti, então bispo auxiliar de Belém e diretor geral da Fundação Nazaré de Comunicação; Elifas Gurgel,
                   representante do Ministério das Comunicações; deputado federal Zenaldo Coutinho, entre outras autoridades 
                   políticas, religiosas, civis e militares.
                </p>

