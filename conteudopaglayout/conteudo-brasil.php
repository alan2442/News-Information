        <h2>Quase dois terços dos consumidores brasileiros não são fiéis a marcas, aponta Google</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia Consumidores brasileiros-->
            <h3>
                A principal razão para incluir um produto no carrinho de compras, apontada por 37% dos entrevistados 
                que não são leais às marcas, é uma boa oferta de preços.
            </h3>
            <img class="imagem-principal" src="imagens/mercado.jpg" alt="" width="100%">
            <div class="texto">

                <p>
                Um levantamento aponta que 64% dos brasileiros não têm marcas preferidas e levam em conta fatores como preço e 
                valores pessoais para fazer suas escolhas de produtos. Na chamada Geração Z (GenZ), formada por nascidos a partir
                 de 1995, a infidelidade a marcas chega a 65%. A conclusão é do estudo Think Consumer Goods, realizado pela 
                 consultoria Offerwise e divulgado nesta terça-feira pelo Google.
                </p>

                <p>
                A principal razão para incluir um produto no carrinho de compras, apontada por 37% dos entrevistados que não são 
                fiéis a marcas, é uma boa oferta de preços.
                </p>

                <p>
                A conexão de marcas com os valores pessoais foi o segundo fator mais citado, por 21% dos entrevistados. Apesar do 
                percentual pouco acima de um quinto do total, a maioria absoluta dos entrevistados (88%) concorda que há marcas 
                capazes de se conectarem melhor com os consumidores do que outras concorrentes.
                </p>

                <p>
                A sustentabilidade foi um aspecto citado por 43% dos entrevistados como diferencial de propósito entre as marcas. 
                Já valores como diversidade e igualdade foram apontados como relevantes para a relação com as marcas por 38% da 
                amostra da pesquisa. Outros 20% citaram propósito e 18% inclusão.
                </p>

                <p>
                O diretor de negócio do Google, Marco Bebiano, aponta que a consolidação de valores é uma ferramenta também para 
                capturar os consumidores em meio à escalada de custos do mercado do consumo, especialmente após o pico de Covid-19
                 e a Guerra da Ucrânia.
                </p>

                <p>
                “É uma discussão que vai além do ESG, se não crio valor pra minha marca as pessoas não vão conseguir perceber o 
                valor que ela tem e não vão querer pagar o que eu acho que ela vale”, afirmou o executivo.
                </p>

                <p>
                De acordo com os entrevistados, a forma mais eficaz de iniciar uma conexão com os consumidores é por descontos e 
                benefícios, apontado por 65% dos entrevistados — o percentual entre mulheres chega a 70%.
                </p>

                <p>
                As experiências de compra mais personalizadas foram um fator para 45%, sendo que 40% dos consumidores escolhem 
                produtos personalizados. Entre a Geração Z, o percentual que opta por personalizados é de 44%. A diretora de GenZ 
                do Google, Maria Dafne, exemplifica produtos personalizados como cadeiras de praia adequadas para pessoas gordas.
                </p>
                
                <p>
                Os consumidores também estão atentos a como o discurso de valores das marcas se reflete em ações concretas. 
                O “falar menos e agir mais” foi destacado por 29% dos entrevistados, fatia que sobe para 35% na Geração Z. A
                 parceria com criadores de conteúdo, mencionada por 17%, também foi apontada como um caminho para criar conexão.
                </p>
                
                <p>
                O streamer Casimiro Miguel, que participou do evento de divulgação da pesquisa, aponta que os criadores de conteúdo 
                podem identificar. Ele exemplifica uma ação de publicidade com a Heinz, executada no horário em que ele realiza suas
                 transmissões diárias. “Acham que ninguém está acordado às 3h da manhã, mas estão sim", exemplifica.
                </p>

                <p>
                A head de marketing transformacional do Zé Delivery, Paula Guz, destacou que o aplicativo da AmBev também busca a 
                fidelização de clientes por meio dos benefícios, como a troca de pontos por ingressos de shows do Coldplay. “Com o 
                Zé fizemos um movimento de construir uma marca que nasce como e-commerce, mas temos que construir o topo do funil,
                 que é o movimento inverso. Tenho desempenho no dia a dia e preciso construir marca”, afirma a executiva.
                </p>
            </div>
            

