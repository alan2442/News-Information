        <h2>Jorge Paulo Lemann, o homem mais rico do Brasil, perde US$ 329 milhões em um dia com ações da Americanas</h2>
        <div class="caixa-conteudo destaque"> <!-- Início noticia Jorge Paulo Lemann-->
            <h3>Sócios de Lemann também perdem milhões em suas fortunas após notícia de rombo contábil na companhia varejista.</h3>
            <img class="imagem-principal" src="imagens/jorge.jpg" alt="" width="100%" >
            <div class="texto">

                <p>
                O economista e empresário Jorge Paulo Lemann, pessoa mais rica do Brasil na atualidade, segundo o ranking de 
                bilionários da Forbes, perdeu US$ 329 milhões — o equivalente a R$ 1,68 bilhão — de sua fortuna de US$ 16 bilhões 
                nesta quinta-feira (12), como consequência da forte queda das ações da Americanas no último pregão da bolsa.
                </p>

                <p>
                Ontem, os papéis da varejista despencaram 77% após a renúncia de Sérgio Rial da presidência apenas 10 dias depois 
                de assumir o cargo, em razão da descoberta de um rombo contábil de R$ 20 bilhões nos balanços da companhia.
                </p>

                <p>
                Marcel Herrmann Telles e Carlos Alberto Sicupira, respectivamente segundo e terceiro homens mais ricos do país, 
                também perderam uma quantia milionária pelo mesmo motivo. Telles viu um recuo de US$ 173 milhões em sua fortuna de
                 US$ 10,8 bilhões, enquanto Sicupira perdeu US$ 199 milhões, ficando com US$ 8,8 bilhões.
                </p>

                <p>
                Os três são sócios na empresa de investimentos 3G Capital e, por meio dela, investem na Americanas e em outras 
                empresas. Antes disso, eles eram controladores da varejista, mas abriram mão do posto na reorganização societária da
                 empresa, que agora é controlada pela B2W.
                </p>

                <p>
                As estimativas do mercado apontam que, juntos, o trio tem uma participação de cerca de 29% na Americanas, o que
                 explica a forte queda em seus patrimônios com a desvalorização das ações.
                </p>

                <p>
                Apesar das baixas, Lemann e os outros "tubarões do mercado" afirmaram que vão continuar com suas posições na 
                companhia, sem se desfazer dos papéis que possuem, porque acreditam no potencial da empresa em se reestruturar.
                </p>

            </div>
            

